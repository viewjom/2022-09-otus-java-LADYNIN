package ru.otus.crm.service;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import ru.otus.cachehw.HwCache;
import ru.otus.core.repository.DataTemplate;
import ru.otus.crm.model.Client;
import ru.otus.core.sessionmanager.TransactionRunner;
import java.lang.reflect.InvocationTargetException;
import java.util.List;
import java.util.Optional;


public class DbServiceClientImpl implements DBServiceClient {
    private static final Logger log = LoggerFactory.getLogger(DbServiceClientImpl.class);

    private final DataTemplate<Client> dataTemplate;
    private final TransactionRunner transactionRunner;

    private final HwCache<String, Client> cache;


    public DbServiceClientImpl(TransactionRunner transactionRunner, DataTemplate<Client> dataTemplate, HwCache<String, Client> cache) {
        this.transactionRunner = transactionRunner;
        this.dataTemplate = dataTemplate;
        this.cache = cache;
    }

    @Override
    public Client saveClient(Client client) {

        return transactionRunner.doInTransaction(connection -> {
            if (client.getId() == null) {

                var clientId = dataTemplate.insert(connection, client);

                var createdClient = new Client(clientId, client.getName());
                log.info("created client: {}", createdClient);

                if (cache != null) {

                    cache.put(String.valueOf(clientId), client);
                }

                return createdClient;
            }
            dataTemplate.update(connection, client);
            log.info("updated client: {}", client);
            return client;
        });
    }

    @Override
    public Optional<Client> getClient(long id) {

        if (cache != null) {
            Client client = cache.get(String.valueOf(id));
            if (client != null) {
                cache.remove(String.valueOf(id));
                return Optional.of(client);
            }
        }


        return transactionRunner.doInTransaction(connection -> {

            Optional<Client> clientOptional = null;
            try {
                clientOptional = dataTemplate.findById(connection, id);
            } catch (IllegalAccessException | InstantiationException | InvocationTargetException | NoSuchMethodException e) {
                throw new RuntimeException(e);
            }
            clientOptional.ifPresent(cl -> cache.put(String.valueOf(id), cl));

            log.info("client: {}", clientOptional);
            return clientOptional;
        });


    }

    @Override
    public List<Client> findAll() {
        return transactionRunner.doInTransaction(connection -> {
            var clientList = dataTemplate.findAll(connection);
            log.info("clientList:{}", clientList);
            return clientList;
        });
    }
}
